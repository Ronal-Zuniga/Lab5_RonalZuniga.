    private void tpStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_tpStateChanged
        if (tp.getSelectedIndex() == 1) {
            refresh();
        }
        if (tp.getSelectedIndex() == 3) {
            if (carreras.isEmpty()) {
                n1.setEnabled(false);
                ap1.setEnabled(false);
                edad.setEnabled(false);
                g.setEnabled(false);
                cn.setEnabled(false);
                c.setEnabled(false);
                JOptionPane.showMessageDialog(null, "Primero debe agregar una carrera");
            } else {
                n1.setEnabled(true);
                ap1.setEnabled(true);
                edad.setEnabled(true);
                g.setEnabled(true);
                cn.setEnabled(true);
                c.setEnabled(true);
                DefaultComboBoxModel m = (DefaultComboBoxModel) c.getModel();
                for (int i = 0; i < carreras.size(); i++) {
                    m.addElement(carreras.get(i).getNombre());
                }
                c.setModel(m);
            }
        }
        if (tp.getSelectedIndex() == 4) {
            DefaultComboBoxModel m = (DefaultComboBoxModel) clases.getModel();
            for (int i = 0; i < cl.size(); i++) {
                m.addElement(cl.get(i));
            }
            clases.setModel(m);
        }
        if (tp.getSelectedIndex() == 5) {
            DefaultListModel modelo
                    = (DefaultListModel) listaclases.getModel();
            for (int i = 0; i < cl.size(); i++) {
                modelo.addElement(cl.get(i));
            }
            listaclases.setModel(modelo);
            DefaultListModel modelo1
                    = (DefaultListModel) listamaestros.getModel();
            for (int i = 0; i < p.size(); i++) {
                if (p.get(i) instanceof Maestro) {
                    modelo1.addElement(cl.get(i));
                }
            }
            listamaestros.setModel(modelo1);
            DefaultListModel modelo2
                    = (DefaultListModel) listaalumnos.getModel();
            for (int i = 0; i < p.size(); i++) {
                if (p.get(i) instanceof Estudiante) {
                    modelo2.addElement(cl.get(i));
                }
            }
            listaalumnos.setModel(modelo2);
        }
    }//GEN-LAST:event_tpStateChanged

    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String facultad = f1.getSelectedItem().toString();
        String tipo = "";
        String jefe = jf.getText();
        double cst = Double.parseDouble(costo.getText());
        if (facultad.equals("Licenciaturas")) {
            tipo = c1.getSelectedItem().toString();
            if (carrera(tipo)) {
                JOptionPane.showMessageDialog(null, "No pueden haber dos carreras del mismo tipo\n"
                        + "La carrera no fue agregada");
            } else {
                Licenciaturas x = new Licenciaturas(tipo, facultad, cst, jefe);
                carreras.add(x);
                JOptionPane.showMessageDialog(null, "Carrera agregada correctamente");
                jf.setText("");
                costo.setText("");
            }
        }
        if (facultad.equals("Ingenierías")) {
            tipo = c1.getSelectedItem().toString();
            if (carrera(tipo)) {
                JOptionPane.showMessageDialog(null, "No pueden haber dos carreras del mismo tipo\n"
                        + "La carrera no fue agregada");
            } else {
                Ingenierias x = new Ingenierias(tipo, facultad, cst, jefe);
                carreras.add(x);
                JOptionPane.showMessageDialog(null, "Carrera agregada correctamente");
                jf.setText("");
                costo.setText("");
            }
        }
        if (facultad.equals("Ciencias de la Salud")) {
            tipo = c1.getSelectedItem().toString();
            if (carrera(tipo)) {
                JOptionPane.showMessageDialog(null, "No pueden haber dos carreras del mismo tipo\n"
                        + "La carrera no fue agregada");
            } else {
                Salud x = new Salud(tipo, facultad, cst, jefe);
                carreras.add(x);
                JOptionPane.showMessageDialog(null, "Carrera agregada correctamente");
                jf.setText("");
                costo.setText("");
            }
        }

    }//GEN-LAST:event_jButton1MouseClicked

