    public boolean carrera(String tipo) {
        boolean r = false;
        for (int i = 0; i < carreras.size(); i++) {
            if (carreras.get(i) instanceof Licenciaturas) {
                if (((Licenciaturas) carreras.get(i)).getTipo().equals(tipo)) {
                    r = true;
                } else {
                    r = false;
                }
            }
            if (carreras.get(i) instanceof Ingenierias) {
                if (((Ingenierias) carreras.get(i)).getTipo().equals(tipo)) {
                    r = true;
                } else {
                    r = false;
                }
            }
            if (carreras.get(i) instanceof Salud) {
                if (((Salud) carreras.get(i)).getTipo().equals(tipo)) {
                    r = true;
                } else {
                    r = false;
                }
            }
        }
        return r;
    }
